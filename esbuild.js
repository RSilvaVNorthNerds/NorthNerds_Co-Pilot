const esbuild = require("esbuild");
const dotenv = require("dotenv");

dotenv.config();

const production = process.argv.includes("--production");
const watch = process.argv.includes("--watch");

/**
 * @type {import('esbuild').Plugin}
 */
const esbuildProblemMatcherPlugin = {
  name: "esbuild-problem-matcher",

  setup(build) {
    build.onStart(() => {
      console.log("[watch] build started");
    });
    build.onEnd((result) => {
      result.errors.forEach(({ text, location }) => {
        console.error(`âœ˜ [ERROR] ${text}`);
        console.error(
          `    ${location.file}:${location.line}:${location.column}:`
        );
      });
      console.log("[watch] build finished");
    });
  },
};

async function main() {
  const ctx = await esbuild.context({
    entryPoints: ["src/extension.ts"],
    bundle: true,
    format: "cjs",
    minify: production,
    sourcemap: !production,
    sourcesContent: false,
    platform: "node",
    outfile: "dist/extension.js",
    external: ["vscode"],
    logLevel: "silent",
    define: {
      "process.env.AWS_BEDROCK_REGION": JSON.stringify(
        process.env.AWS_BEDROCK_REGION
      ),
      "process.env.AWS_BEDROCK_ACCESS_KEY_ID": JSON.stringify(
        process.env.AWS_BEDROCK_ACCESS_KEY_ID
      ),
      "process.env.AWS_BEDROCK_SECRET_ACCESS_KEY": JSON.stringify(
        process.env.AWS_BEDROCK_SECRET_ACCESS_KEY
      ),
    },
    plugins: [
      /* add to the end of plugins array */
      esbuildProblemMatcherPlugin,
    ],
  });
  if (watch) {
    await ctx.watch();
  } else {
    await ctx.rebuild();
    await ctx.dispose();
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
